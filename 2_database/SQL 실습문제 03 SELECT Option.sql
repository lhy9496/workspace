--16. 환경조경학과 전공과목들의 과목 별 평점
SELECT DEPARTMENT_NO
FROM TB_DEPARTMENT
WHERE DEPARTMENT_NAME = '환경조경학과'; --034

SELECT CLASS_NO, CLASS_NAME, "AVG(POINT)"
FROM(SELECT CLASS_NO, AVG(POINT) AS "AVG(POINT)"
    FROM TB_GRADE
    GROUP BY CLASS_NO)
JOIN TB_CLASS USING (CLASS_NO)
WHERE DEPARTMENT_NO = 034 AND CLASS_TYPE = '전공선택'
ORDER BY CLASS_NO;

--17. 최경희 학생과 같은 과 학생들의 이름과 주소
SELECT STUDENT_NAME, STUDENT_ADDRESS
FROM TB_STUDENT
WHERE DEPARTMENT_NO = (SELECT DEPARTMENT_NO
                        FROM TB_STUDENT
                        WHERE STUDENT_NAME = '최경희');

--18.국어국문학과(001) 총 평점이 가장 높은 학생의 이름과 학번
SELECT STUDENT_NO, STUDENT_NAME
FROM (SELECT STUDENT_NO, AVG(POINT) AS "AVG(POINT)"
      FROM TB_GRADE
      GROUP BY STUDENT_NO
      ORDER BY AVG(POINT) DESC)
JOIN TB_STUDENT USING (STUDENT_NO)
WHERE DEPARTMENT_NO = 001
AND ROWNUM = 1;

--19. 환경조경학과가 속한 계열 학과(자연과학)들의 학과별 전공과목 평점
--GROUP BY DEPARTMENT_NAME
--CATEGORY = '자연과학'
SELECT DEPARTMENT_NAME AS "계열 학과명", ROUND(AVG(POINT), 1)
FROM(SELECT DEPARTMENT_NAME, CATEGORY, CLASS_NO, POINT
     FROM TB_DEPARTMENT
     JOIN TB_CLASS USING(DEPARTMENT_NO)
     JOIN TB_GRADE USING(CLASS_NO)
     WHERE CATEGORY = '자연과학')
GROUP BY DEPARTMENT_NAME;


